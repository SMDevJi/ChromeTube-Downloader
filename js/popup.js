function reduceVideoDetails(allData) {

    function formatSeconds(seconds) {
        const hours = Math.floor(seconds / 3600);
        const minutes = Math.floor((seconds % 3600) / 60);
        const remainingSeconds = seconds % 60;

        const paddedHours = String(hours).padStart(2, '0');
        const paddedMinutes = String(minutes).padStart(2, '0');
        const paddedSeconds = String(remainingSeconds).padStart(2, '0');

        return `${paddedHours}:${paddedMinutes}:${paddedSeconds}`;
    }

    detailsExtracted = {}
    detailsExtracted.title = allData.videoDetails.title
    detailsExtracted.thumbnailUrl = allData.videoDetails.thumbnail.thumbnails[allData.videoDetails.thumbnail.thumbnails.length - 1].url
    detailsExtracted.length = formatSeconds(allData.videoDetails.lengthSeconds)

    detailsExtracted.videoQualities = []
    allData.streamingData.formats.forEach(format => {
        extractedFormat = {}
        if (format.qualityLabel.includes('144p') || format.qualityLabel.includes('360p') || format.qualityLabel.includes('240p')) {
            extractedFormat.name = `${format.qualityLabel} ${(format.bitrate / 1024) > 1024 ? Math.trunc(Math.trunc(format.bitrate / 1024) / 1024) + 'Mbps' : Math.trunc(format.bitrate / 1024) + 'Kbps'}`
        }
        else if (format.qualityLabel.includes('480p')) {
            extractedFormat.name = `${format.qualityLabel} SD ${(format.bitrate / 1024) > 1024 ? Math.trunc(Math.trunc(format.bitrate / 1024) / 1024) + 'Mbps' : Math.trunc(format.bitrate / 1024) + 'Kbps'}`
        }
        else {
            extractedFormat.name = `${format.qualityLabel} HD ${(format.bitrate / 1024) > 1024 ? Math.trunc(Math.trunc(format.bitrate / 1024) / 1024) + 'Mbps' : Math.trunc(format.bitrate / 1024) + 'Kbps'}`
        }

        extractedFormat.isNoAudio = false
        extractedFormat.link = format.url

        detailsExtracted.videoQualities.push(extractedFormat)
    })


    detailsExtracted.audioQualities = []
    detailsExtracted.videoWithoutAudioQualities = []

    allData.streamingData.adaptiveFormats.forEach(format => {
        if (format.mimeType.includes('audio')) {
            extractedFormat = {}
            extractedFormat.name = `${Math.trunc(format.bitrate / 1024)}Kbps`

            extractedFormat.isNoAudio = false
            extractedFormat.link = format.url

            detailsExtracted.audioQualities.push(extractedFormat)
        }
        else {
            extractedFormat = {}
            if (format.qualityLabel.includes('144p') || format.qualityLabel.includes('360p') || format.qualityLabel.includes('240p')) {
                extractedFormat.name = `${format.qualityLabel} ${(format.bitrate / 1024) > 1024 ? Math.trunc(Math.trunc(format.bitrate / 1024) / 1024) + 'Mbps' : Math.trunc(format.bitrate / 1024) + 'Kbps'}`
            }
            else if (format.qualityLabel.includes('480p')) {
                extractedFormat.name = `${format.qualityLabel} SD ${(format.bitrate / 1024) > 1024 ? Math.trunc(Math.trunc(format.bitrate / 1024) / 1024) + 'Mbps' : Math.trunc(format.bitrate / 1024) + 'Kbps'}`
            }
            else {
                extractedFormat.name = `${format.qualityLabel} HD ${(format.bitrate / 1024) > 1024 ? Math.trunc(Math.trunc(format.bitrate / 1024) / 1024) + 'Mbps' : Math.trunc(format.bitrate / 1024) + 'Kbps'}`
            }

            extractedFormat.isNoAudio = true
            extractedFormat.link = format.url

            detailsExtracted.videoWithoutAudioQualities.push(extractedFormat)
        }



    })


    return detailsExtracted;
}






async function getFileSize(url) {
    function formatBytes(bytes) {
        if (bytes === 0) return '0 B';

        const units = ['B', 'KB', 'MB', 'GB', 'TB', 'PB', 'EB', 'ZB', 'YB'];
        const i = Math.floor(Math.log(bytes) / Math.log(1024));

        return (bytes / Math.pow(1024, i)).toFixed(2) + ' ' + units[i];
    }
    try {
        const response = await fetch(url, { method: 'HEAD' });

        if (!response.ok) {
            throw new Error(`Unable to fetch metadata from ${url}. Status: ${response.status}`);
        }

        const contentLength = response.headers.get('Content-Length');

        if (!contentLength) {
            throw new Error('Content-Length header is not available.');
        }

        const sizeInBytes = parseInt(contentLength, 10);
        const humanReadableSize = formatBytes(sizeInBytes);

        return humanReadableSize;

    } catch (error) {
        console.error('Error getting file size:', error);
        return null;
    }
}


function sanitizeFilename(filename) {
    // Replace non-ASCII characters and any other system-restricted characters (e.g., /\:*?"<>|)
    return filename.replace(/[^\x00-\x7F]|[\\\/:*?"<>|]/g, '_');
}


function objectToQueryString(params) {
    const searchParams = new URLSearchParams();

    for (const [key, value] of Object.entries(params)) {
        searchParams.append(key, value);
    }

    return searchParams.toString();
}


function extractYtInitialPlayerResponse(html) {
    const regex = /var\s+ytInitialPlayerResponse\s*=\s*(\{.*?\});/s;
    const match = html.match(regex);

    if (match && match[1]) {
        try {
            return JSON.parse(match[1]);
        } catch (error) {
            console.error("Error parsing JSON:", error);
            return null;
        }
    }

    return null;
}

function extractJsUrl(inputString) {
    // Regular expression to match the "jsUrl" and extract the URL that follows
    const regex = /"jsUrl"\s*:\s*"([^"]+)"/;
    const match = inputString.match(regex);

    if (match) {
        // Return the URL if found
        return match[1];
    } else {
        // Return null if no match found
        return null;
    }
}

function extractVisitorData(html) {
    // Regular expression to find visitorData value within quotes
    const regex = /"visitorData":"(.*?)"/;
    const match = html.match(regex);

    // If match is found, return the extracted value; otherwise, return null
    if (match && match[1]) {
        return match[1];
    } else {
        return null; // Return null if no visitorData is found
    }
}




